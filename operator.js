// [ while loop ]
let i = 3;
while (i > 0) {
    console.log(`num: ${i}`);
    i--;
}

// [ for loop ]
// * for(begin; condition; step)
for (i=5; i > 0; i--) {
    console.log(`for: ${i}`);
}

// [ if문]
// * if (condition) { statement1 }

let name = "gilbert"

